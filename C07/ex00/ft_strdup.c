#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;

	while (src[i])
	{
		i++;
	}
	dest = malloc(i * sizeof(char) + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
